static void MX_GPIO_Init(void);

/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* MCU Configuration--------------------------------------------------------*/
  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();
  /* Configure the system clock */
  SystemClock_Config();
  /* Initialize all configured peripherals */
  MX_GPIO_Init();

  // testing SPI1 setup because COPI is correct
//  uint16_t digital = 0x3FFF;

  uint16_t keypadNum = -1;

  // initialize the SPI peripheral to communicate with the DAC
  DAC_init();
  // initialize keypad
  uint16_t volt = 0;
  uint8_t voltdigi = 0;
  uint16_t analog = 0;

  keypadInit();

  while (1)
  {
	  // read keypad and place into correct 10s place
	  keypadNum = keyMap(keyPressed());

	  if( (keypadNum > 9) || (keypadNum < 0) )		// invalid number bounds
	  {
		  keypadNum = -1;
	  }
	  else
	  {
		  for(int i = NUMINPUTS; i > voltdigi; i--)
		  {
			  keypadNum *= 10;
		  }
		  volt += keypadNum;
		  voltdigi++;

		  // got the 3rd voltage input
		  if(voltdigi == NUMINPUTS)
		  {
			  analog = DAC_volt_conv(volt);
			  DAC_calibrate(analog, volt);

			  DAC_write(analog);

			  keypadNum = -1;
			  voltdigi = 0;
			  volt = 0;
		  }
	  }

	  HAL_Delay(100);

	  // testing DAC write works
//	  DAC_write(digital);
  }
}

